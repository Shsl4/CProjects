string getPatternName(int pattern) {

    switch (pattern) {

        case 0:
            return "Line";
        case 1:
            return "U";
        case 2:
            return "Vessel";
        case 3:
            return "Random";
        case 4:
            return "Random (Vertically Symmetric)";
        case 5:
            return "Random (Horizontally Symmetric)";
    }

    return "Invalid";

}
